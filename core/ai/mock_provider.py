def _fmt_constraints(constraints: List[str]) -> str:
    if not constraints:
        return "No constraints specified."
    return "\n".join(f"  - {c}" for c in constraints)


def _fmt_expected(expected: str) -> str:
    return expected if expected.strip() else "Structured Markdown document."


def _build_header(request: ProviderRequest, doc_title: str) -> str:
    return (
        f"# {doc_title}\n\n"
        f"**Agent Role:** {request.role}  \n"
        f"**Task:** {request.task}  \n"
        f"**Generated by:** MockProvider v1  \n\n"
        "---\n\n"
        f"## Objective\n\n"
        f"{request.objective}\n"
    )


def _build_footer(request: ProviderRequest) -> str:
    parts = []
    if request.has_constraints():
        parts.append(
            "## Constraints Observed\n\n"
            + _fmt_constraints(request.constraints)
        )
    if request.has_expected_output():
        parts.append(
            f"## Expected Output Format\n\n"
            f"{request.expected_output}"
        )
    parts.append(
        "---\n\n"
        "_Generated by MockProvider -- AI Company OS_"
    )
    return "\n\n".join(parts)


