class MockProvider(AIProvider):
    """
    Deterministic AI provider for testing and development.

    Returns structured Markdown responses based on keyword detection in the
    ProviderRequest.task field.  Identical inputs always produce identical
    outputs.  No randomness, no networking, no AI.

    The provider is always healthy (health() returns True).  It never raises
    ProviderUnavailableError.  It raises InvalidRequestError only if the
    request fails basic validation.
    """

    _PROVIDER_NAME = "MockProvider"
    _MODEL_NAME = "mock-v1"
    _TEMPERATURE = 0.0

    def name(self) -> str:
        """Return the stable provider identifier."""
        return self._PROVIDER_NAME

    def health(self) -> bool:
        """Always returns True -- MockProvider is always available."""
        return True

    def generate(self, request: ProviderRequest) -> ProviderResponse:
        """
        Generate a deterministic response for the given request.

        Args:
            request: The structured input from the agent.

        Returns:
            A ProviderResponse with deterministic content.

        Raises:
            InvalidRequestError: If the request fails validation.
        """
        try:
            request.validate()
        except Exception as exc:
            raise InvalidRequestError(str(exc)) from exc

        content = self._generate_content(request)
        tokens_used = len(content.split())
        exec_time = self._compute_execution_time(request)

        return ProviderResponse(
            content=content,
            tokens_used=tokens_used,
            provider_name=self._PROVIDER_NAME,
            execution_time=exec_time,
            metadata=self._make_metadata(request),
        )

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _generate_content(self, request: ProviderRequest) -> str:
        """Select and render the appropriate response template."""
        category = _detect_category(request.task)
        generator = _GENERATORS.get(category, _default_response)
        return generator(request)

    def _compute_execution_time(self, request: ProviderRequest) -> float:
        """Compute a deterministic execution time from the request task."""
        base = 0.05
        step = (len(request.task) % 10) * 0.01
        return round(base + step, 3)

    def _make_metadata(self, request: ProviderRequest) -> Dict[str, Any]:
        """Build provider metadata for the response."""
        return {
            "model": self._MODEL_NAME,
            "temperature": self._TEMPERATURE,
            "deterministic": True,
            "provider": self._PROVIDER_NAME,
            "category": _detect_category(request.task),
            "role": request.role,
            "constraint_count": request.constraint_count(),
        }
