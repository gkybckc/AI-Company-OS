def demo_engine_history(engine) -> None:
    print_divider()
    print("ENGINE HISTORY & STATISTICS")
    print_divider()
    print()

    history = engine.history()
    print(f"Total artifacts generated: {len(history)}")
    print()
    print("Artifact log:")
    for a in history:
        label = ArtifactType(a.type).label()
        proj = a.project_id[:8] + "..." if a.project_id else "N/A"
        print(f"  [{a.version:2d}] {label:<35} | project: {proj} | words: {a.word_count()}")

    print()
    stats = engine.statistics()
    print("Type counts:")
    for t, count in stats["type_counts"].items():
        if count > 0:
            print(f"  {t:<35} : {count}")
    print(f"Unique projects: {stats['unique_projects']}")
    print(f"Unique generators: {stats['unique_generators']}")
    print()


