_DIVIDER = "=" * 70
_THIN = "-" * 70


def print_divider(label: str = "") -> None:
    if label:
        pad = (_DIVIDER[:3] + " " + label + " ").ljust(69) + "="
        print(pad)
    else:
        print(_DIVIDER)


def print_artifact_summary(artifact) -> None:
    s = artifact.summary()
    print(f"  Type         : {s['type']}")
    print(f"  Title        : {s['title']}")
    print(f"  Version      : {s['version']}")
    print(f"  Project ID   : {s['project_id'] or 'N/A (company-wide)'}")
    print(f"  Generated by : {s['generated_by']}")
    print(f"  Words        : {s['word_count']}")
    print(f"  Lines        : {s['line_count']}")
    print(f"  Created at   : {s['created_at']}")
    print(f"  Artifact ID  : {s['id']}")


def print_content_preview(artifact, lines: int = 25) -> None:
    content_lines = artifact.content.splitlines()
    preview = content_lines[:lines]
    for line in preview:
        print(line)
    if len(content_lines) > lines:
        remaining = len(content_lines) - lines
        print(f"  ... [{remaining} more lines]")


