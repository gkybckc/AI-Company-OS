def setup_engines():
    """Create and seed all engines for the demo."""
    print("Setting up engines...")

    # Executive Engine + Project
    ee = ExecutiveEngine()
    proj = ee.create_project(
        title="E-Commerce Platform",
        description=(
            "Build a full-featured e-commerce platform with product catalog, "
            "shopping cart, user authentication, payment processing, and order management."
        ),
        objective=(
            "Deliver a production-ready e-commerce platform with Stripe payments, "
            "OAuth2 authentication, real-time inventory tracking, and mobile-responsive design."
        ),
        priority=Priority.HIGH,
    )

    t1 = ee.create_task(
        proj.id, "User Authentication Module",
        "Implement JWT-based authentication with OAuth2 social login support.",
        "Alice Chen", Priority.HIGH,
    )
    t2 = ee.create_task(
        proj.id, "Product Catalog Service",
        "Build CRUD API for products with search, filtering, and pagination.",
        "Bob Kumar", Priority.HIGH, dependencies=[t1.id],
    )
    t3 = ee.create_task(
        proj.id, "Payment Gateway Integration",
        "Integrate Stripe payment processing with webhooks and refund support.",
        "Alice Chen", Priority.CRITICAL, dependencies=[t1.id],
    )
    t4 = ee.create_task(
        proj.id, "Order Management System",
        "Build order creation, tracking, and fulfillment workflows.",
        "Carol Diaz", Priority.HIGH, dependencies=[t2.id, t3.id],
    )
    t5 = ee.create_task(
        proj.id, "Frontend Shopping Cart",
        "React-based shopping cart with real-time inventory validation.",
        "Carol Diaz", Priority.MEDIUM, dependencies=[t2.id],
    )

    ee.assign_task(t1.id, "Alice Chen")
    ee.assign_task(t2.id, "Bob Kumar")
    ee.assign_task(t3.id, "Alice Chen")

    print(f"  Project created: {proj.title} (ID: {proj.id[:8]}...)")
    print(f"  Tasks created: {len(proj.tasks)}")

    # Memory Engine
    me = MemoryEngine()
    me.store(MemoryEntry(
        id=str(uuid4()),
        title="Architecture Decision: Microservices vs Monolith",
        category=MemoryCategory.DECISION,
        scope=MemoryScope.GLOBAL,
        author="CEO",
        content="Decided to start with a modular monolith and extract services when scale demands it.",
        tags={"architecture", "decision", "monolith"},
        created_at=datetime.now(timezone.utc),
        updated_at=datetime.now(timezone.utc),
    ))
    me.store(MemoryEntry(
        id=str(uuid4()),
        title="Lesson: Test-Driven Development improves delivery speed",
        category=MemoryCategory.LESSON,
        scope=MemoryScope.GLOBAL,
        author="EngineeringLead",
        content="Writing tests first reduced debugging time by 40% in Sprint 12.",
        tags={"lesson", "testing", "tdd"},
        created_at=datetime.now(timezone.utc),
        updated_at=datetime.now(timezone.utc),
    ))
    print(f"  Memory entries: {me.count()}")

    # Decision Engine
    de = DecisionEngine(memory_engine=me)
    dec = de.create_decision(
        title="Choose payment processor for e-commerce platform",
        options=[
            DecisionOption(
                title="Stripe",
                advantages=["Developer-friendly API", "Strong fraud detection", "Excellent docs"],
                disadvantages=["Higher per-transaction fee"],
                estimated_risk="LOW",
                estimated_cost="MEDIUM",
            ),
            DecisionOption(
                title="PayPal",
                advantages=["Wide user adoption", "Established trust"],
                disadvantages=["Complex API", "Redirect-based checkout"],
                estimated_risk="LOW",
                estimated_cost="LOW",
            ),
        ],
        project_id=proj.id,
    )
    de.evaluate(dec.id)
    de.recommend(dec.id)
    print(f"  Decision created: {dec.title}")

    # Workflow Engine
    we = WorkflowEngine(memory_engine=me, decision_engine=de)
    wf = we.create_workflow(
        name="E-Commerce Platform Build",
        description="Full development lifecycle for the e-commerce platform.",
        template=WorkflowTemplate.SOFTWARE_PROJECT,
    )
    we.start(wf.id)
    we.advance(wf.id)
    print(f"  Workflow started: {wf.name}")

    # Planner Engine
    pe = PlannerEngine()
    print("  PlannerEngine ready.")

    # Artifact Engine (full configuration)
    engine = ArtifactEngine(
        executive_engine=ee,
        planner_engine=pe,
        workflow_engine=we,
        memory_engine=me,
        decision_engine=de,
    )

    return engine, proj.id


